"두 개의 주사위 합이 5인 경우를 구하기"

"Finding the case where the sum of two dice is 5"

for(let i = 1; i < 7; i++){
    for(let j = 1; j < 7; j++){
        if(i+j==5)
        console.log(`${i}+${j}=${5}`);
    }
}


